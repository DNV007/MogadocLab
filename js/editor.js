function handleDroppedFile(file){
  if(!file) return;
  const reader=new FileReader();
  reader.onload=ev=>{
    try{
      loadStructureText(file.name, ev.target.result, { displayName: file.name.replace(/\.[^.]+$/,'') });
    }catch(err){notify('Import failed: '+err.message,'error');}
  };
  reader.readAsText(file);
}
fileDrop.addEventListener('drop',e=>{e.preventDefault();fileDrop.classList.remove('drag-over');handleDroppedFile(e.dataTransfer.files[0]);});
const canvasArea=document.getElementById('canvasArea');
canvasArea.addEventListener('dragover',e=>e.preventDefault());
canvasArea.addEventListener('drop',e=>{e.preventDefault();handleDroppedFile(e.dataTransfer.files[0]);});

