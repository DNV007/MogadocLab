let notifTimer=null;
function notify(msg,type='success'){
  const el=document.getElementById('notification'),icons={success:'✓',error:'✕',info:'ℹ'};
  document.getElementById('notifIcon').textContent=icons[type]||'';
  document.getElementById('notifMsg').textContent=msg;
  el.className='show '+type;
  if(notifTimer)clearTimeout(notifTimer);
  notifTimer=setTimeout(()=>el.className='',3500);
}
function clearMolecule(){
  if(typeof clearTrajectory==='function' && trajectoryFrames?.length){
    clearTrajectory();
  }
  molecule=null;
  document.getElementById('emptyState').style.display='flex';
  const mb=document.getElementById('moleculeName');mb.textContent='No structure loaded';mb.classList.remove('loaded');
  document.getElementById('headerFormula').textContent='';document.getElementById('headerFormula').classList.remove('visible');
  ['statAtoms','statBonds','statElems'].forEach(id=>document.getElementById(id).textContent='0');
  ['statMW','statFormula'].forEach(id=>document.getElementById(id).textContent='—');
  document.getElementById('atomLegend').innerHTML='<div style="font-family:\'JetBrains Mono\',monospace;font-size:10px;color:var(--text-muted);">Load a molecule to see elements</div>';
  render();
  saveAppState();
}

